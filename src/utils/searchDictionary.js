/**
 * 將輸入的查詢字串展開為搜尋詞列表
 * 輸入：「壓克力 立牌」
 * 輸出：["壓克力 立牌", "アクリル", "アクリルスタンド"]
 */
export function expandQuery(input) {
  const raw = input.trim().toLowerCase()
  if (!raw) return []

  const terms = new Set([raw])

  // 對照表匹配（支援包含匹配，不需完全相等）
  for (const [zh, jaList] of Object.entries(dictionary)) {
    if (raw.includes(zh.toLowerCase())) {
      jaList.forEach((ja) => terms.add(ja.toLowerCase()))
    }
  }

  return Array.from(terms)
}
